import "regenerator-runtime/runtime";
import type { Metadata } from "next";
import { Inter } from "next/font/google";
import { Poppins } from "next/font/google";
import "./globals.css";

// Provider
import { Providers } from "../providers";
import AiBotMain from "@/components/AiChatBot/AiBotMain";
import { Analytics } from '@vercel/analytics/react';

import { notFound } from "next/navigation";
import { NextIntlClientProvider, useMessages } from "next-intl";
import { locales } from "@/navigation";
import ToastProvider from "@/utils/ToastProvider";
import 'react-datetime-picker/dist/DateTimePicker.css';
import 'react-calendar/dist/Calendar.css';
import 'react-clock/dist/Clock.css';

const inter = Inter({
  subsets: ["latin"],
  variable: "--font-inter",
  display: "swap",
});

const poppins = Poppins({
  subsets: ["latin"],
  variable: "--font-poppins",
  display: "swap",
  weight: "400",
});

export const metadata: Metadata = {
  title: "Clinca San Miguel",
  description: "Generated by create next app",
};

export default function RootLayout({
  children,
  params: { locale },
}: {
  children: React.ReactNode;
  params: { locale: string };
}) {
  if (!locales.includes(locale)) {
    notFound();
  }

  const messages = useMessages();

  return (
    <html lang={locale} className={`${inter.variable} ${poppins.variable}`}>
      <head> <link rel="icon" href="/favicon.png"  /></head>
      <NextIntlClientProvider locale={locale} messages={messages}>
        <body className="font-inter bg-[#F8F5F0] relative overflow-x-hidden w-[100vw]">
          <Providers>
            <ToastProvider>
              {" "}
              
              {children}
              <div
                className="sticky z-[999999] bottom-10 right-10"
                style={{ zIndex: 999999 }}
              >
                <AiBotMain />
                <Analytics mode={'production'} />
              </div>
            </ToastProvider>
          </Providers>
        </body>
      </NextIntlClientProvider>
    </html>
  );
}
